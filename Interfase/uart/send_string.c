#include "send_string.h"

//---------------------------------------------------------------------------------------
void send_int(unsigned int data)
{
	char str[5];
	unsigned char dat[5]={0};
	for (unsigned char i=0;i<5;i++)
	{
		//---------------------------------------------------------------------------------------
		dat[i]=data%10;
		data=data/10;
		str[i] = dat[i] + '0';
		//---------------------------------------------------------------------------------------
	};
	//---------------------------------------------------------------------------------------
	for (unsigned char i=5;i>0;i--)
	{
		//---------------------------------------------------------------------------------------
		uart_write(str[i-1]);
		//---------------------------------------------------------------------------------------
	};
	//---------------------------------------------------------------------------------------
};
